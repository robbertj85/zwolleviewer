const ZH_GEOSERVER = "https://geodata.zuid-holland.nl/geoserver";

function zh(
  workspace: string,
  typeName: string,
  city: CityConfig,
  maxFeatures = 500,
  full = false
) {
  return fetchPDOKWFS(
    `${workspace}:${typeName}`,
    `${ZH_GEOSERVER}/${workspace}/wfs`,
    city.bboxWFS,
    maxFeatures,
    full
  );
}

